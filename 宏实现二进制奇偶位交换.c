#include <stdio.h>
                    //用宏来实现整数二进制的奇数位和偶数位的交换
#define jiaoh(x) ((((x)&0x55555555)<<1)|(((x)&0xaaaaaaaa)>>1))
//整数的奇数位按位与&一个奇数位都是1偶数位都是0的数(0x55555555)得到奇数位
//整数的偶数位按位与&一个偶数位都是1奇数位都是0的数(0xaaaaaaaa)得到偶数位
//奇数位左移一位得到奇数位变成偶数位的情况,因为左移去掉了一个偶数位,补了个0
//偶数位右移一位得到偶数位变成奇数位的情况,因为右移去掉了一个奇数位,补了个0
//将两个结果按位或|，得到奇数位和偶数位交换的结果


int main()
{
    int kaka = 11;
    printf("%d\n", jiaoh(kaka));
    return 0;
}